package baekjoon;

import java.util.Scanner;
public class baekjoon_10950 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		processMath(a);
	}
	public static void processMath(int a) {
		int sum= 0;
		for(int i = 1 ; i <= a; i++ ){
			sum += i;
		
		}
		System.err.println(sum);
	}

}
